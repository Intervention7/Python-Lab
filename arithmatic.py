from math import sqrt

print("Addition of {0} and {1} is {2}".format(100,200,100+200))
print("Multiplication of {0} and {1} is {2}".format(100,200,100*200))
print("Division of {0} and {1} is  {2}".format(100,200,100//200))
print("Subtraction of {0} and {1} is {2}".format(100,200,100-200))
print("exponent of {0} and {1} is {2}".format(10,10,10**2))
print("Square root of {0} is {1}".format(4,sqrt(4)))
